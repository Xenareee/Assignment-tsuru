<h1>Czemu muszę czekać? Powtórki SRS</h1>
<div class="explore-article">
Powtórki na <span class="pink"><i>tsuru!</i></span> używają pewnej zasady. Czym lepiej pamiętasz pewien znak kanji, tym dłuższy będzie odstęp do następnej powtórki. 
<br><br>
To jest właśnie działanie <span class="pink">SRS</span> (ang. <i>Spaced-Repetition-System</i>), z którego skuteczności korzysta wiele aplikacji do nauki języków. Ma on swoje korzenie w ludzkiej naturze: jest nam trudniej zapomnieć pewną informację, jeżeli uda nam się przywołać po coraz dłuższych odstępach czasu.
<br><br><br>
Na tej stronie jest możliwość pominięcia systemu i powtórzenia znaków na żądanie. <b>Jednak jeżeli nie znasz tych znaków z innych źródeł, jest to odradzane.</b>
