<h1>Podstawy kanji</h1>
<div class="explore-article">
<span class="pink">Znaki kanji</span> to symbole zapożyczone do języka japońskiego z Chin. Sama ich nazwa (漢字 rom. <i>kanji</i>) dosłownie tłumaczona oznacza "chińskie symbole". Symboli tych są tysiące, ale w codziennym życiu używane są około 2 tysiące.
<br><br>
Każdy symbol ma przyporządkowane sobie <span class="pink">znaczenie</span>: te właśnie znaczenia są podane w <a href="index.php?page=kanji">słowniczku</a>.
<br><br>
<span class="pink">Słowa</span> to znaki ułożone w konkretnej kolejności i z konkretną wymową, i w większości przypadków znaczenia kanji składają się na znaczenie słowa. Bez znajomości znaków w ich składzie trudno byłoby się czegokolwiek nauczyć!
<br><br>
<div style="text-align: center;">
    <span class="pink"><b>Na przykład:</b></span><br>
    大 (duży) + 人 (człowiek) = 大人 [おとな rom. <i>otona</i>] (dorosły)
</div>
<br><br>
Same znaki często składają się z części, które wyglądają jak inne znaki. Te części często nazywane są <span class="pink">radykałami</span>, choć niektóre słowniki uznają tylko jedną część w znaku za radykał.
<br><br>
<div style="text-align: center;">
    <span class="pink"><b>Na przykład:</b></span><br>
    大 (duży)
    <br>
    犬 (pies)
</div>
<br><br>
Proponuję wykorzystać ten fakt, i tworzyć własne skojarzenia pomagające w zapamiętaniu znaków kanji. Na tej stronie poznasz kilka znaków, które potem pojawiają się w innych.
